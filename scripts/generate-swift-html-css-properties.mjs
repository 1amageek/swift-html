#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { mkdir } from "node:fs/promises";
import { tmpdir } from "node:os";
import path from "node:path";

const defaultVersion = "8.0.3";
const defaultOutput = "Sources/SwiftHTML/Elements/StyleProperties.generated.swift";
const swiftKeywords = new Set([
  "Any",
  "Self",
  "Type",
  "actor",
  "as",
  "associatedtype",
  "async",
  "await",
  "break",
  "case",
  "catch",
  "class",
  "continue",
  "default",
  "defer",
  "deinit",
  "do",
  "else",
  "enum",
  "extension",
  "fallthrough",
  "false",
  "fileprivate",
  "for",
  "func",
  "if",
  "import",
  "in",
  "init",
  "inout",
  "internal",
  "is",
  "let",
  "nil",
  "nonisolated",
  "open",
  "operator",
  "private",
  "protocol",
  "public",
  "repeat",
  "rethrows",
  "return",
  "self",
  "some",
  "static",
  "struct",
  "subscript",
  "super",
  "switch",
  "throw",
  "throws",
  "true",
  "try",
  "typealias",
  "var",
  "where",
  "while",
]);

const args = parseArgs(process.argv.slice(2));
const version = args.version ?? defaultVersion;
const outputPath = path.resolve(args.output ?? defaultOutput);
const data = args.input
  ? JSON.parse(readFileSync(path.resolve(args.input), "utf8"))
  : readBrowserCompatData(version);

const generated = generateSwiftStyleProperties(data, version);

if (args.check) {
  const current = readFileSync(outputPath, "utf8");
  if (current !== generated) {
    console.error(`${path.relative(process.cwd(), outputPath)} is out of date.`);
    console.error("Run: node scripts/generate-swift-html-css-properties.mjs");
    process.exit(1);
  }
  process.exit(0);
}

await mkdir(path.dirname(outputPath), { recursive: true });
writeFileSync(outputPath, generated, "utf8");

function parseArgs(values) {
  const parsed = {};
  for (let index = 0; index < values.length; index += 1) {
    const value = values[index];
    switch (value) {
      case "--input":
        parsed.input = values[++index];
        break;
      case "--output":
        parsed.output = values[++index];
        break;
      case "--version":
        parsed.version = values[++index];
        break;
      case "--check":
        parsed.check = true;
        break;
      default:
        throw new Error(`Unknown argument: ${value}`);
    }
  }
  return parsed;
}

function readBrowserCompatData(version) {
  const directory = mkdtempSync(path.join(tmpdir(), "swift-html-css-properties-"));
  try {
    const packageName = execFileSync(
      "npm",
      ["pack", `@mdn/browser-compat-data@${version}`, "--silent", "--pack-destination", directory],
      { encoding: "utf8" },
    ).trim();
    execFileSync("tar", ["-xzf", path.join(directory, packageName), "-C", directory]);
    return JSON.parse(readFileSync(path.join(directory, "package", "data.json"), "utf8"));
  } finally {
    rmSync(directory, { recursive: true, force: true });
  }
}

function generateSwiftStyleProperties(data, version) {
  const properties = data.css?.properties;
  if (!properties) {
    throw new Error("Expected MDN browser compatibility data at css.properties.");
  }

  const records = Object.entries(properties)
    .filter(([name, entry]) => includesProperty(name, entry))
    .map(([name]) => ({
      property: name,
      helper: swiftIdentifier(forCSSProperty(name)),
    }))
    .sort((left, right) => left.property.localeCompare(right.property));

  assertUnique(records, "property");
  assertUnique(records, "helper");

  const lines = [
    "// Generated by scripts/generate-swift-html-css-properties.mjs.",
    `// Source: @mdn/browser-compat-data ${version} css.properties.`,
    "// Filter: standard_track == true, deprecated != true, vendor-prefixed properties excluded.",
    "// Do not edit this file manually.",
    "",
    "public extension Style {",
    ...records.map((record) =>
      `    static func ${record.helper}(_ value: String) -> Style { Style.property("${record.property}", value) }`,
    ),
    "}",
    "",
  ];

  return lines.join("\n");
}

function includesProperty(name, entry) {
  if (name.startsWith("__") || name.startsWith("-")) {
    return false;
  }
  const status = entry.__compat?.status ?? {};
  return status.standard_track === true && status.deprecated !== true;
}

function forCSSProperty(name) {
  return name
    .split("-")
    .map((part, index) => {
      if (index === 0) {
        return part;
      }
      return part.charAt(0).toUpperCase() + part.slice(1);
    })
    .join("");
}

function swiftIdentifier(name) {
  const escaped = /^[0-9]/.test(name) ? `_${name}` : name;
  return swiftKeywords.has(escaped) ? `\`${escaped}\`` : escaped;
}

function assertUnique(records, key) {
  const seen = new Map();
  for (const record of records) {
    const value = record[key];
    const existing = seen.get(value);
    if (existing) {
      throw new Error(`Duplicate generated ${key}: ${value} for ${existing.property} and ${record.property}`);
    }
    seen.set(value, record);
  }
}
